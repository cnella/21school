#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*p1;

	if (lst)
	{
		p1 = lst;
		while (p1)
		{
			f(p1);
			p1 = p1->next;
		}
	}
}
