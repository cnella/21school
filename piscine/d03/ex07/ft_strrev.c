#include <unistd.h>
char	*ft_strrev(char *str)
{
	int		i;
	int		k;
	char	s;

	i = 0;
	k = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (i != 0)
	{
		s = str[i];
		str[i] = str[k];
		str[k] = s;
		i--;
		k++;
	}
	return ("*str");
}
