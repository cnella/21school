int		ft_iterative_factorial(int nb)
{
	int i;

	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	i = 1;
	while (nb >= 1)
	{
		i = i * nb;
		nb--;
	}
	return (i);
}
