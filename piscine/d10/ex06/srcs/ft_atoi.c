int			ft_atoi(char *str)
{
	int		nb;
	int		neg;
	int		i;
	int		k;

	i = 0;
	neg = 1;
	nb = 0;
	k = 0;
	while (str[i] == '\n' || str[i] == ' ' || str[i] == '\t' ||
		str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == 45)
		neg = -1;
	if (str[i] == 45 || str[i] == 43)
		i++;
	while (str[i] > 47 && str[i] < 58)
	{
		nb = nb * 10 + ((int)str[i] - 48);
		i++;
		k++;
	}
	if (k > 19)
		return (-1);
	return (nb * neg);
}
